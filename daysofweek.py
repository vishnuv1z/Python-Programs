day = input("Enter the day of the week: ")
if (day=="Sunday" or day=="sunday"):
    print("It is the start of the week")
elif (day=="Monday" or day=="monday"):
    print("It is the second day of the week")
elif (day=="Tuesday" or day=="tuesday"):
    print("It is the third day of the week")
elif (day=="Wednesday" or day=="wednesday"):
    print("It is the Midday of the week")
elif (day=="Thursday" or day=="thursday"):
    print("It is the fifth day of the week")
elif (day=="Friday" or day=="friday"):
    print("It is almost end of the week")
elif (day=="Saturday" or day=="saturday"):
    print("It is the end of the week")
else:
    print("Invalid day.")